func lengthOfLongestSubstring(s string) int {
	if len(s) == 0{
		return 0
	}
	m := map[byte]int{}
	res, start := 0, 0
	for end := 0; end < len(s); end ++{
		if _, exit := m[s[end]]; exit{
			start = max(start, m[s[end]] + 1)
		}
		m[s[end]] = end
		res = max(res, end - start + 1)
	}
	return res
}
func max(a int, b int) int {
	if a > b{
		return a
	}
	return b
}
